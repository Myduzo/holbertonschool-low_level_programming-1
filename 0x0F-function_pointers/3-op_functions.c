#include "calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - function for adding
 * @a: integer type
 * @b: integer type
 * Description: This program will return
 * a sum after adding of a & b.
 * Return: Sum
 */
int op_add(int a, int b);
{
	return (a + b);
}

/**
 * op_sub - function for subtracting
 * @a: integer type
 * @b: integer type
 * Description: This program will return
 * a difference after subtraction of a & b.
 * Return: Difference
 */
int op_sub(int a, int b);
{
	return (a - b);
}

/**
 * op_mul - function for multiplying
 * @a: integer type
 * @b: integer type
 * Description: This program will return
 * a product after multiplication of a & b.
 * Return: Product
 */
int op_mul(int a, int b);
{
	return (a * b);
}

/**
 * op_div - function for dividing
 * @a: integer type
 * @b: integer type
 * Description: This program will return
 * a quotient after division of a by b.
 * Return: Quotient
 */
int op_div(int a, int b);
{
	return (a / b);
}

/**
 * op_mod - function for a remainder
 * @a: integer type
 * @b: integer type
 * Description: This program will return
 * a remainder as a result of division.
 * Return: Remainder
 */
int op_mod(int a, int b);
{
	return (a % b);
}
